function deleteDuplicates(head: ListNode | null): ListNode | null {
  let cur = head
  while (cur !== null && cur.next !== null) {
    if (cur.val === cur.next.val) {
      let delNode: any = cur.next
      cur.next = delNode.next
      delNode = null
    }
    else {
      cur = cur.next
    }
  }

  return head
}
